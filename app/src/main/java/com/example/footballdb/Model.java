package com.example.footballdb;

public class Model {
    String strTeam;
    String strTeamLogo;
    String idTeam;
    String strDescriptionEN;
    int intFormedYear;
    String strCountry;
    String strStadium;

    public String getStrTeam(){
        return strTeam;
    }
    public void setStrTeam(String strTeam) {
        this.strTeam = strTeam;
    }
    public String getStrTeamLogo(){
        return strTeamLogo;
    }
    public void setStrTeamLogo(String strTeamLogo) {
        this.strTeamLogo = strTeamLogo;
    }
    public String getIdTeam(){
        return idTeam;
    }
    public void setIdTeam(String idTeam) {
        this.idTeam = idTeam;
    }
    public String getStrDescriptionEN(){
        return strDescriptionEN;
    }
    public void setStrDescriptionEN(String strDescriptionEN) {
        this.strDescriptionEN = strDescriptionEN;
    }
    public int getIntFormedYear(){
        return intFormedYear;
    }
    public void setIntFormedYear(int intFormedYear) {
        this.intFormedYear = intFormedYear;
    }
    public String getStrCountry(){
        return strCountry;
    }
    public void setStrCountry(String strCountry) {
        this.strCountry = strCountry;
    }
    public String getStrStadium(){
        return strStadium;
    }
    public void setStrStadium(String strStadium) {
        this.strStadium = strStadium;
    }
}
